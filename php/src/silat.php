<p style="text-align: justify; padding-left: 30px;">Ekstrakulikuler Pencak Silat SMAN 1 CERDAS BERKARYA merupakan salah satu ekskul yang ada di SMAN 1 CERDAS BERKARYA. Ekskul pencak Silat ini memiliki jadwal Kamis pukul 14:00 WIB setiap minggu nya dan terdiri atas baik putra maupun putri . Didalam kegiatan nya ekskul pencak silat di bina dan dilatih oleh guru  yang ada di SMAN 1 Cerdas Berkarya yang sudah kompeten dalam melatih. Dalam perjalanan nya ekskul pencak silat telah banyak mengikuti perlombaan dengan mendapatkan berbagai prestasi. Dan yang terbaru ekskul pencak silat SMAN 1 Cerdas Berkarya mendapatkan juara 1 baik Tingkat Provinsi dan Nasional Tahun 2019.</p><br>
