<p style="text-align: justify; padding-left: 30px;">Ekstrakulikuler sepakbola SMAN 1 CERDAS BERKARYA merupakan salah satu ekskul yang ada di SMAN 1 CERDAS BERKARYA. Ekskul sepakbola ini memiliki jadwal senin, rabu, dan jumat pukul 16.00 WIB setiap minggu nya. Didalam kegiatan nya ekskul sepakbola di bina dan dilatih oleh guru yang ada di SMAN 1 Cerdas Berkarya yang sudah kompeten dalam melatih. Dalam perjalanan nya ekskul sepakbola telah banyak mengikuti perlombaan dengan mendapatkan berbagai prestasi. Dan yang terbaru ekskul sepakbola SMAN 1 Cerdas Berkarya mendapatkan juara 2 Liga Pendidikan Indonesia Tingkat Kota Malang Tahun 2019.</p><br>
