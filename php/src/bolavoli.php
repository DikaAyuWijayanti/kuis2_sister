<p style="text-align: justify; padding-left: 30px;">Ekstrakulikuler bola voli SMAN 1 CERDAS BERKARYA merupakan salah satu ekskul yang ada di SMAN 1 CERDAS BERKARYA. Ekskul bola voli ini memiliki jadwal selasa, kamis pukul 14.00 WIB setiap minggu nya dan terdiri atas kelompok bola voli perempuan dan kelompok bola voli putra . Didalam kegiatan nya ekskul bola voli di bina dan dilatih oleh guru yang ada di SMAN 1 Cerdas Berkarya yang sudah kompeten dalam melatih. Dalam perjalanan nya ekskul bola voli telah banyak mengikuti perlombaan dengan mendapatkan berbagai prestasi. Dan yang terbaru ekskul bola voli SMAN 1 Cerdas Berkarya mendapatkan juara 1 Liga Pendidikan Indonesia Tingkat Kota Lamongan Tahun 2019.</p><br>
