<p style="text-align:center;"><strong>Sekilas Komite Sekolah &#8230;<span id="more-1316"></span></strong></p>
<p style="text-align:justify;">Keputusan Menteri Pendidikan Nasional RI No. 044/U/2002 memutuskan bahwa pada setiap kabupaten/kota dibentuk Dewan Pendidikan atas prakarsa masyarakat dan/atau pemerintah kabupaten/kota. Dan pada setiap satuan pendidikan atau kelompok satuan pendidikan dibentuk Komite Sekolah atas prakarsa masyarakat, satuan pendidikan dan/atau pemerintah kabupaten.kota. Berpijak dari hal itu maka SMAN 1 CERDAS BERKARYA membentuk Komite Sekolah yang awalnya bernama BP3 (Badan Pengurus Pembangunan Pendidikan) yang kepengurusannya di ketuai oleh Bapak Nandang Sonjaya.</p>
<p style="text-align:justify;">Selain pembangunan infrastruktur fisik Komite Sekolah juga ikut membantu menaikkan kesejahteraan guru, karyawan dan kepala sekolah, guru tidak tetap dan karyawan tidak tetap.</p>
<p><strong>Visi, Misi &amp; Tujuan Komite SMAN 1 CERDAS BERKARYA<br />
</strong></p>
<p>Visi                  : Menggalang Peran serta masyarakat dalam meningkatkan mutu pendidikan.</p>
<p>Misi                 : Menunjang &amp; merealisasikan mutu pendidikan yang lebih baik di SMAN 1 CERDAS BERKARYA</p>
<p>Tujuan            :</p>
<ul>
<li style="text-align:justify;">Menampung aspirasi &amp; prakarsa masyarakat dengan melahirkan kebijakan operasional dan peran pendidikan.</li>
</ul>
<ul>
<li style="text-align:justify;">Meningkatkan tanggungjawab masyarakat dan menciptakan kondisi transparansi akutansi dalam penyelenggaraan pendidikan bermutu di sekolah.<strong> </strong></li>
</ul>
<p>Fungsi Komite sekolah:</p>
<ul>
<li style="text-align:justify;">Memberikan pertimbangan kepada sekolah dalam proses penyelenggaraan pendidikan.</li>
</ul>
<ul>
<li style="text-align:justify;">Memberikan dukungan finansial dalam bentuk dana dan tenaga dalam penyelenggaraan pendidikan.</li>
</ul>
<ul>
<li style="text-align:justify;">Mengadakan kontrol tentang transparansi penyelenggaraan pendidikan dibidang dana.</li>
</ul>
<ul>
<li style="text-align:justify;">Menjadi mediator antara Pemerintah dan Masyarakat yang peduli pendidikan.</li>
</ul>
<ul>
<li style="text-align:justify;">Melaksanakan kerjasama dengan masyarakat untuk menyelenggarakan pendidikan.</li>
</ul>
<ul>
<li>Memberi masukan tentang :</li>
</ul>
<p style="padding-left:30px;">1. Kebijakan-kebijakan sekolah yang berhubungan dengan pendidikan<br />
2. R.A.P.B.S</p>
<ul>
<li>Rekomendasi tentang:</li>
</ul>
<p style="padding-left:30px;">1. Kriteria kinerja di satuan pendidikan.<br />
2 .Tenaga Pendidikan yang kompeten dengan pendidikan.<br />
3. Fasilitas Pendidikan.</p>
<ul>
<li style="text-align:justify;">Menggalang dana masyarakat dalam pembiayaan penyelenggaraan pendidikan.</li>
</ul>
<ul>
<li style="text-align:justify;">Mengevaluasi dan mengadakan pengawasan kebijakan program penyelenggaraan pendidikan   serta  output pendidikan.</li>
</ul>
