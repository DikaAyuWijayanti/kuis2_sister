<p style="text-align: justify; padding-left: 30px;">Ekstrakulikuler Pramuka SMAN 1 CERDAS BERKARYA merupakan salah satu ekskul yang ada di SMAN 1 CERDAS BERKARYA. Ekskul pramuka ini memiliki jadwal sabtu pukul 14:00 WIB setiap minggu nya. Didalam kegiatan nya ekskul pramuka di bina dan dilatih oleh guru dan kakak DAP  yang ada di SMAN 1 Cerdas Berkarya yang sudah kompeten dalam melatih. Dalam perjalanan nya ekskul pramuka telah banyak mengikuti perlombaan dengan mendapatkan berbagai prestasi. Dan yang terbaru ekskul pramuka SMAN 1 Cerdas Berkarya mendapatkan juara 1 Lomba yeyel, semaphore pada Tahun 2019.</p><br>
