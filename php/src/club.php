<p style="text-align: justify; padding-left: 30px;">Ekstrakulikuler English Club SMAN 1 CERDAS BERKARYA merupakan salah satu ekskul yang ada di SMAN 1 CERDAS BERKARYA. Ekskul english club ini memiliki jadwal hari Kamis pukul 15:00 WIB setiap minggu nya . Didalam kegiatan nya ekskul english club di bina dan dilatih oleh guru beserta Mrs.Corin yang berasal dari amerika serikat  yang ada di SMAN 1 Cerdas Berkarya yang sudah kompeten dalam melatih. Dalam perjalanan nya ekskul english club telah banyak mengikuti perlombaan dengan mendapatkan berbagai prestasi. Dan yang terbaru ekskul english club SMAN 1 Cerdas Berkarya mendapatkan juara umum lomba English Festival Part 5 IPB 2019 yang diselenggarakan oleh IPB  yang terdiri dari Juara 1 Story Telling , Juara 2 Sing A Song, Juara 3 News Casting serta melakukan kunjungan ke US.Consulate General Surabaya/ EDUCATIONUSA Surabaya .</p><br>
